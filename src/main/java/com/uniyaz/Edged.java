package com.uniyaz;

public abstract class Edged extends Shape {
    private int length;
    private int width;

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    @Override
    public String toString() {
        return "length=" + length +
                ", width=" + width +
                ", "+super.toString();
    }
}
